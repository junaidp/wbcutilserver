package com.wbc.utilserver.model.switchprobe;

public class PortData {
    private int portNum;
    private int portIf;

    public PortData() {
    }

    public int getPortNum() {
        return portNum;
    }

    public void setPortNum(int portNum) {
        this.portNum = portNum;
    }

    public int getPortIf() {
        return portIf;
    }

    public void setPortIf(int portIf) {
        this.portIf = portIf;
    }
}
